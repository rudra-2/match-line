import React from 'react'

// Animated Icon Wrapper
interface IconWrapperProps {
    children: React.ReactNode
    animate?: 'pulse' | 'bounce' | 'spin' | 'float' | 'shake' | 'glow' | 'none'
    size?: 'sm' | 'md' | 'lg' | 'xl'
    className?: string
}

export const IconWrapper: React.FC<IconWrapperProps> = ({
    children,
    animate = 'none',
    size = 'md',
    className = '',
}) => {
    const sizes = {
        sm: 'w-5 h-5',
        md: 'w-6 h-6',
        lg: 'w-8 h-8',
        xl: 'w-12 h-12',
    }

    const animations = {
        none: '',
        pulse: 'animate-pulse',
        bounce: 'animate-icon-bounce',
        spin: 'animate-spin',
        float: 'animate-icon-float',
        shake: 'animate-icon-shake',
        glow: 'animate-icon-glow',
    }

    return (
        <span className={`inline-flex items-center justify-center ${sizes[size]} ${animations[animate]} ${className}`}>
            {children}
        </span>
    )
}

