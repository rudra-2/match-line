// Animated Icon Container for special effects
interface AnimatedIconContainerProps {
    children: React.ReactNode
    variant?: 'glow' | 'pulse-ring' | 'float' | 'bounce' | 'rotate'
    color?: string
    className?: string
}

export const AnimatedIconContainer: React.FC<AnimatedIconContainerProps> = ({
    children,
    variant = 'glow',
    color = 'var(--color-accent-primary)',
    className = '',
}) => {
    const variants = {
        glow: 'icon-container-glow',
        'pulse-ring': 'icon-container-pulse-ring',
        float: 'icon-container-float',
        bounce: 'icon-container-bounce',
        rotate: 'icon-container-rotate',
    }

    return (
        <div
            className={`
        relative inline-flex items-center justify-center
        rounded-2xl p-4
        bg-[var(--color-surface-sunken)]
        ${variants[variant]}
        ${className}
      `}
            style={{ '--icon-color': color } as React.CSSProperties}
        >
            {children}
            {variant === 'pulse-ring' && (
                <>
                    <span className="absolute inset-0 rounded-2xl border-2 border-current opacity-30 animate-ping" style={{ color }} />
                </>
            )}
        </div>
    )
}

